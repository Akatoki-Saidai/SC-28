#!/bin/bash

# ==========================================
# 設定：自分のプロジェクトフォルダのパスに書き換える
# ==========================================
PROJECT_DIR="/home/pi/RepoName"

# 1. プロジェクトへ移動
cd "$PROJECT_DIR" || { echo "フォルダが見つかりません"; sleep 3; exit 1; }

echo "-----------------------------------"
echo "GitHubから最新を取得中..."
echo "-----------------------------------"

# 2. 最新情報の取得 (Fetch) と 統合 (Pull)
git fetch origin
git pull origin main

# 3. 仮想環境を有効化
source .venv/bin/activate

echo "-----------------------------------"
echo "完了！仮想環境に入りました。"
echo "-----------------------------------"

# 4. シェルを維持する（これにより黒い画面が閉じずに残る）
exec bash